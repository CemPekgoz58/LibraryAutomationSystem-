    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       String searchTerm = jTextField1.getText().trim().toLowerCase();
    dlm.clear();

    EntityManagerFactory emf = Persistence.createEntityManagerFactory("ComputerProject2PU");
    EntityManager em = emf.createEntityManager();

    try {
        em.getTransaction().begin();
        //find book by title
        List<Books> results = em.createQuery(
            "SELECT b FROM Books b WHERE LOWER(b.booksPK.title) LIKE :search", Books.class)
            .setParameter("search", "%" + searchTerm + "%")
            .getResultList();

        for (Books b : results) {
            dlm.addElement(b.getBooksPK().getTitle() + " - " + b.getBooksPK().getAuthor() +
                           " - " + b.getPageCount() + " pages - " + b.getCategory());
                    em.persist(b);

        }
        em.getTransaction().commit();

        if (results.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No matching books found.");
        }

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Search Error: " + ex.getMessage());
    }
        em.close();
        emf.close();
    

    }//GEN-LAST:event_jButton3ActionPerformed

