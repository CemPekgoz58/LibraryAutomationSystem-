    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        dlm.clear();

    EntityManagerFactory emf = Persistence.createEntityManagerFactory("ComputerProject2PU");
    EntityManager em = emf.createEntityManager();

    try {
        List<Books> allBooks = em.createQuery("SELECT b FROM Books b", Books.class).getResultList();

        for (Books b : allBooks) {
            dlm.addElement(b.getBooksPK().getTitle() + " - " + b.getBooksPK().getAuthor() +
                           " - " + b.getPageCount() + " pages - " + b.getCategory());
        }

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Database Error: " + ex.getMessage());
    } finally {
        em.close();
        emf.close();
    }

    }//GEN-LAST:event_jButton2ActionPerformed

